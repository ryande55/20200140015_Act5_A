package com.example.popupmenudanlistview;

public class ClassNama {
    private String Nama;

    //inisiasi objek
    public ClassNama(String nama)
    {
        this.Nama = nama;
    }

    //Membuat method getName untuk mengembalikan nilai ke variabel Nama
    public String getName()
    {
        return this.Nama;
    }

}
